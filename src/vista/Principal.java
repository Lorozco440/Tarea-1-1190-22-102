    void modificar() {
        String dpi = txtDpi.getText();
        String Nom = txtNombres.getText();
        String Ape = txtApellidos.getText();
        String Clin = txtClinica.getText();
        String Consu = txtConsulta.getText();
        String Fe = txtFecha.getText();
        
        //sentencia sql para modificar
        String sql = "update pacientes set Dpi='" + dpi + "', Nombres='" + Nom + "',Apellidos='" + Ape + "',Clinica='" + Clin + "',Consulta='" + Consu + "',Fecha='" + Fe + "', where IdPaciente=" + id;
        if (dpi.equals("") || Nom.equals("") || Ape.equals("")|| Clin.equals("")|| Consu.equals("")|| Fe.equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar datos");
        } else {
            try {
                cn = con.getConnection();
                st = cn.createStatement();  
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Paciente Actualizado");
                limpiartabla();//limpia la tabla despues de modificar
            } catch (Exception e) {
            }
    }
        }
