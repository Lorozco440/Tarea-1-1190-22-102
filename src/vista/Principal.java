    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        int fila = TablaDatos.getSelectedRow();
        if (fila ==-1) {
            JOptionPane.showMessageDialog(null, "Usuario no Seleccionado");
        } else {
            id = Integer.parseInt((String) TablaDatos.getValueAt(fila, 0).toString());
            String dpi = (String) TablaDatos.getValueAt(fila, 1);
            String Nom = (String) TablaDatos.getValueAt(fila, 2);
            String Ape = (String) TablaDatos.getValueAt(fila, 3);
            String Clin= (String) TablaDatos.getValueAt(fila, 4);
            String Consu = (String) TablaDatos.getValueAt(fila, 5);
            String Fe= (String) TablaDatos.getValueAt(fila, 6);
            txtId.setText("" + id);
            txtDpi.setText(dpi);
            txtNombres.setText(Nom);
            txtApellidos.setText(Ape);
            txtClinica.setText(Clin);
            txtConsulta.setText(Consu);
            txtFecha.setText(Fe);
            
        }
    }//GEN-LAST:event_TablaDatosMouseClicked
    
